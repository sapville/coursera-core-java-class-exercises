package com.javaoo.calculators;

public class TrigonometricCalculator {

    public double sine(double arg) {
        return Math.sin(arg);
    }

    public double cosine(double arg) {
        return Math.cos(arg);
    }

    public double tangent(double arg) {
        return Math.tan(arg);
    }

    public double arcsine(double arg) {
        return Math.asin(arg);
    }

    public double arccosine(double arg) {
        return Math.acos(arg);
    }

    public double arctangent(double arg) {
        return Math.atan(arg);
    }

}
